use std::collections::BTreeSet;
use std::path::{Path, PathBuf};

use async_trait::async_trait;
use codegraph_core::traits::GraphQuerier;
use serde::Serialize;

use crate::error::Result;
use crate::generate::render_template_with_project;
use crate::generate::traits::{GeneratedFile, GlobalGenerator};
use crate::generate::GenerationEntry;
use crate::generate::ProjectConfig;
use codegraph_config::DomainConfig;

#[derive(Debug, Serialize)]
pub struct GrpcScaffoldContext {
    pub domains: Vec<GrpcScaffoldDomain>,
}

#[derive(Debug, Serialize)]
pub struct GrpcScaffoldDomain {
    pub name: String,
}

pub struct GrpcScaffoldGenerator {
    output_dir: PathBuf,
}

impl GrpcScaffoldGenerator {
    pub fn new(output_dir: &Path) -> Self {
        Self {
            output_dir: output_dir.to_path_buf(),
        }
    }
}

#[async_trait]
impl GlobalGenerator for GrpcScaffoldGenerator {
    fn name(&self) -> &str {
        "grpc_scaffold"
    }

    async fn generate(
        &self,
        _db: &dyn GraphQuerier,
        _config: &DomainConfig,
        generation_order: &[GenerationEntry],
        tera: &tera::Tera,
        project: &ProjectConfig,
    ) -> Result<Vec<GeneratedFile>> {
        // Collect unique domains from generation_order
        let domain_names: BTreeSet<String> = generation_order
            .iter()
            .map(|e| e.domain.clone())
            .collect();

        let mut domains: Vec<GrpcScaffoldDomain> = domain_names
            .into_iter()
            .map(|name| GrpcScaffoldDomain { name })
            .collect();
        domains.sort_by(|a, b| a.name.cmp(&b.name));

        // Only generate if there are domains with gRPC-capable entities
        if generation_order.is_empty() {
            return Ok(Vec::new());
        }

        let ctx = GrpcScaffoldContext {
            domains,
        };

        let proto_dir = self.output_dir.join("proto");
        let grpc_dir = self.output_dir.join("src").join("api").join("grpc");

        let mut files = Vec::new();

        // Generate proto/shared.proto
        let shared_content =
            render_template_with_project(tera, "grpc/proto_shared.tera", &ctx, project)?;
        files.push(GeneratedFile {
            path: proto_dir.join("shared.proto"),
            content: shared_content,
        });

        // Generate src/api/grpc/mod.rs
        let mod_content = render_grpc_mod(&ctx);
        files.push(GeneratedFile {
            path: grpc_dir.join("mod.rs"),
            content: mod_content,
        });

        // Generate src/api/grpc/shared.rs — conversion helpers
        let convert_content = generate_convert_helpers();
        files.push(GeneratedFile {
            path: grpc_dir.join("shared.rs"),
            content: convert_content,
        });

        Ok(files)
    }
}

fn render_grpc_mod(ctx: &GrpcScaffoldContext) -> String {
    let mut out = String::from("// Generated by codegraph. DO NOT EDIT.\n\n");
    out.push_str("pub mod shared;\n");
    out.push_str("pub mod convert;\n\n");

    for domain in &ctx.domains {
        out.push_str(&format!(
            "pub mod {} {{\n    tonic::include_proto!(\"{}\");\n}}\n\n",
            domain.name, domain.name
        ));
    }

    for domain in &ctx.domains {
        out.push_str(&format!("pub mod {}_router;\n", domain.name));
    }

    out
}

fn generate_convert_helpers() -> String {
    r#"// Generated by codegraph. DO NOT EDIT.

use uuid::Uuid;

/// Convert a UUID to a proto string.
pub fn uuid_to_string(id: Uuid) -> String {
    id.to_string()
}

/// Parse a proto string to a UUID.
pub fn string_to_uuid(s: &str) -> Result<Uuid, String> {
    Uuid::parse_str(s).map_err(|e| e.to_string())
}

/// Convert a prost_types::Timestamp to chrono::NaiveDateTime.
pub fn timestamp_to_chrono(ts: prost_types::Timestamp) -> chrono::NaiveDateTime {
    chrono::DateTime::from_timestamp_nanos(ts.seconds * 1_000_000_000 + ts.nanos as i64)
        .naive_utc()
}

/// Convert a chrono::NaiveDateTime to prost_types::Timestamp.
pub fn chrono_to_timestamp(dt: chrono::NaiveDateTime) -> prost_types::Timestamp {
    prost_types::Timestamp {
        seconds: dt.and_utc().timestamp(),
        nanos: 0,
    }
}

/// Serialize a Decimal to a proto string.
pub fn decimal_to_string(d: rust_decimal::Decimal) -> String {
    d.to_string()
}

/// Parse a proto string to a Decimal.
pub fn string_to_decimal(s: &str) -> Result<rust_decimal::Decimal, String> {
    rust_decimal::Decimal::from_str(s).map_err(|e| e.to_string())
}
"#.to_string()
}
