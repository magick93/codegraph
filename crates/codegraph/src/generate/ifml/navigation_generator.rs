use std::path::{Path, PathBuf};

use async_trait::async_trait;
use codegraph_core::traits::GraphQuerier;
use serde::Serialize;

use crate::error::Result;
use crate::generate::render_template;
use crate::generate::traits::{GeneratedFile, GlobalGenerator};
use crate::generate::GenerationEntry;
use codegraph_config::DomainConfig;

use super::querier::*;

pub struct IfmlNavigationGenerator {
    output_dir: PathBuf,
}

impl IfmlNavigationGenerator {
    pub fn new(output_dir: &Path) -> Self {
        Self {
            output_dir: output_dir.to_path_buf(),
        }
    }
}

#[async_trait]
impl GlobalGenerator for IfmlNavigationGenerator {
    fn name(&self) -> &str {
        "ifml-navigation"
    }

    async fn generate(
        &self,
        db: &dyn GraphQuerier,
        _config: &DomainConfig,
        _generation_order: &[GenerationEntry],
        tera: &tera::Tera,
    ) -> Result<Vec<GeneratedFile>> {
        let querier = IfmlGraphQuerier::new(db);

        // Query the full IFML model
        let model = querier.get_ifml_model().await.map_err(|e| {
            crate::error::Error::Graph(e)
        })?;

        if model.view_containers.is_empty() {
            return Ok(vec![]);
        }

        // Build the route map context
        let route_map = build_route_map(&model);

        let mut files = Vec::new();

        // Navigation route map
        if let Ok(content) = render_navigation_map(tera, &route_map) {
            files.push(GeneratedFile {
                path: PathBuf::from("src/lib/routes.ts"),
                content,
            });
        }

        // Type-safe route helpers
        let type_helpers = generate_type_helpers(&route_map);
        files.push(GeneratedFile {
            path: PathBuf::from("src/lib/route-helpers.ts"),
            content: type_helpers,
        });

        Ok(files)
    }
}

#[derive(Debug, Serialize)]
struct RouteMapContext {
    views: Vec<ViewRouteContext>,
    nav_links: Vec<NavLinkContext>,
}

#[derive(Debug, Serialize)]
struct ViewRouteContext {
    name: String,
    route: String,
    label: String,
    is_landmark: bool,
    is_modal: bool,
    params: Vec<RouteParamContext>,
}

#[derive(Debug, Serialize)]
struct RouteParamContext {
    name: String,
    type_ref: String,
}

#[derive(Debug, Serialize)]
struct NavLinkContext {
    from: String,
    to: String,
    event: String,
    label: String,
}

fn build_route_map(model: &super::context::IfmlModel) -> RouteMapContext {
    let views: Vec<ViewRouteContext> = model
        .view_containers
        .iter()
        .map(|vc| ViewRouteContext {
            name: vc.name.clone(),
            route: to_kebab_case(&vc.name),
            label: vc.label.clone().unwrap_or_else(|| vc.name.clone()),
            is_landmark: vc.is_landmark,
            is_modal: vc.is_modal,
            params: vc
                .params
                .iter()
                .map(|p| RouteParamContext {
                    name: p.name.clone(),
                    type_ref: p.type_ref.clone(),
                })
                .collect(),
        })
        .collect();

    let nav_links: Vec<NavLinkContext> = model
        .navigation_edges
        .iter()
        .map(|edge| NavLinkContext {
            from: edge.source_container.clone(),
            to: edge.target_container.clone(),
            event: edge.source_event.clone(),
            label: edge.source_event.clone(),
        })
        .collect();

    RouteMapContext { views, nav_links }
}

fn render_navigation_map(tera: &tera::Tera, ctx: &RouteMapContext) -> Result<String> {
    if tera.get_template("ifml/navigation_map.tera").is_ok() {
        render_template(tera, "ifml/navigation_map.tera", ctx)
    } else {
        Ok(generate_navigation_map_inline(ctx))
    }
}

fn generate_navigation_map_inline(ctx: &RouteMapContext) -> String {
    let mut s = String::new();

    // Type definitions for routes
    s.push_str("// Generated by codegraph IFML generator.\n");
    s.push_str("// DO NOT EDIT.\n\n");

    s.push_str("export interface RouteDefinition {\n");
    s.push_str("  name: string;\n");
    s.push_str("  route: string;\n");
    s.push_str("  label: string;\n");
    s.push_str("  isLandmark: boolean;\n");
    s.push_str("  isModal: boolean;\n");
    s.push_str("  params: string[];\n");
    s.push_str("}\n\n");

    // Route map constant
    s.push_str("export const routeMap: Record<string, RouteDefinition> = {\n");
    for view in &ctx.views {
        s.push_str(&format!("  '{}': {{\n", view.name));
        s.push_str(&format!("    name: '{}',\n", view.name));
        s.push_str(&format!("    route: '/{}',\n", view.route));
        s.push_str(&format!("    label: '{}',\n", view.label));
        s.push_str(&format!("    isLandmark: {},\n", view.is_landmark));
        s.push_str(&format!("    isModal: {},\n", view.is_modal));
        s.push_str("    params: [");
        for (i, p) in view.params.iter().enumerate() {
            if i > 0 {
                s.push_str(", ");
            }
            s.push_str(&format!("'{}'", p.name));
        }
        s.push_str("],\n");
        s.push_str("  },\n");
    }
    s.push_str("};\n\n");

    // Navigation link helper
    s.push_str("export interface NavLink {\n");
    s.push_str("  from: string;\n");
    s.push_str("  to: string;\n");
    s.push_str("  event: string;\n");
    s.push_str("  label: string;\n");
    s.push_str("}\n\n");

    // Navigation links constant from edges
    s.push_str("export const navigationLinks: NavLink[] = [\n");
    for link in &ctx.nav_links {
        s.push_str(&format!(
            "  {{ from: '{}', to: '{}', event: '{}', label: '{}' }},\n",
            link.from, link.to, link.event, link.label
        ));
    }
    s.push_str("];\n\n");

    // Helper function
    s.push_str("export function resolveRoute(routeName: string, params?: Record<string, string>): string {\n");
    s.push_str("  const def = routeMap[routeName];\n");
    s.push_str("  if (!def) throw new Error(`Unknown route: ${routeName}`);\n");
    s.push_str("  let path = def.route;\n");
    s.push_str("  if (params) {\n");
    s.push_str("    for (const [key, value] of Object.entries(params)) {\n");
    s.push_str("      path = path.replace(`:${key}`, encodeURIComponent(value));\n");
    s.push_str("    }\n");
    s.push_str("  }\n");
    s.push_str("  return path;\n");
    s.push_str("}\n");

    s
}

fn generate_type_helpers(_ctx: &RouteMapContext) -> String {
    let mut s = String::new();

    s.push_str("// Generated by codegraph IFML generator.\n");
    s.push_str("// DO NOT EDIT.\n\n");

    s.push_str("import { resolveRoute } from './routes';\n\n");

    s.push_str("/**\n");
    s.push_str(" * Type-safe navigation helper.\n");
    s.push_str(" * Call with route name and optional path params.\n");
    s.push_str(" */\n");
    s.push_str("export function navigate(routeName: string, params?: Record<string, string>): string {\n");
    s.push_str("  return resolveRoute(routeName, params);\n");
    s.push_str("}\n\n");

    s.push_str("/**\n");
    s.push_str(" * Build a URL path with query string.\n");
    s.push_str(" */\n");
    s.push_str("export function buildUrl(routeName: string, params?: Record<string, string>, query?: Record<string, string>): string {\n");
    s.push_str("  let path = resolveRoute(routeName, params);\n");
    s.push_str("  if (query) {\n");
    s.push_str("    const qs = new URLSearchParams(query).toString();\n");
    s.push_str("    if (qs) path += '?' + qs;\n");
    s.push_str("  }\n");
    s.push_str("  return path;\n");
    s.push_str("}\n");

    s
}

fn to_kebab_case(s: &str) -> String {
    let mut result = String::new();
    for (i, c) in s.chars().enumerate() {
        if c.is_uppercase() {
            if i > 0 {
                result.push('-');
            }
            result.push(c.to_ascii_lowercase());
        } else {
            result.push(c);
        }
    }
    result
}
